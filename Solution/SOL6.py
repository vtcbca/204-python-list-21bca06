# WAS to enter any number from 1 to 7 and print appropiate day name.

n=int(input("Enter any number:"))
if(n==1):
    print("SUNday")
elif(n==2):
    print("Monday")
elif(n==3):
    print("Tuesday")
elif(n==4):
    print("Wedesday")
elif(n==5):
    print("Thrusday")
elif(n==6):
    print("Friday")
elif(n==7):
    print("Saturday")
else:
    print("Invalid number")
